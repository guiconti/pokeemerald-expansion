const GENERATIONS = ["RB", "GS", "RS", "DP", "BW", "XY", "SM", "SS", "SV"];

function sumPokemonBaseStats(pokemon) {
	return pokemon.hp + pokemon.atk + pokemon.def + pokemon.spa + pokemon.spd + pokemon.spe;
}

const ALTS = {};

function generateList(pokemons, listName, countName) {
	let content = `#define ${countName} ARRAY_COUNT(${listName})\nstatic const u16 ${listName}[] = {\n`;
	for (let i = 0; i < GENERATIONS.length; i++) {
		const generationName = GENERATIONS[i];
		content += `#if P_GEN_${i + 1}_POKEMON == TRUE\n`;
		const pokemonsInGeneration = pokemons.filter((pokemon) => {
			const formattedName = pokemon.name
				.toUpperCase()
				.replace(/[^A-Z0-9]+/gi, " ")
				.trim();
			return pokemon.oob
				? pokemon.oob.genfamily[0] === generationName
				: ALTS[formattedName].oob.genfamily[0] === generationName;
		});
		for (const pokemon of pokemonsInGeneration) {
			const formattedName = pokemon.name
				.toUpperCase()
				.replace(/[^A-Z0-9]+/gi, " ")
				.trim();
			const species = normalPokemonToC[formattedName];
			if (!species) {
				console.log(`Missing species for ${formattedName}`);
				throw new Error(`Missing species for ${formattedName}`);
			}
			content += `\t${species},\n`;
		}
		content += `#endif\n`;
	}
	content += "};\n\n";
	return content;
}

