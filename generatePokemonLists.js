function generatePokemonLists(genNumber) {
	const dexSettings = JSON.parse(fs.readFileSync("./dexSettings.json", "utf8"));
	const pokemons = dexSettings.injectRpcs[1][1].pokemon;
	// Weak to strong
	const sortedByPowerPokemons = pokemons
		.sort((a, b) => sumPokemonBaseStats(a) - sumPokemonBaseStats(b))
		.filter(
			(pokemon) =>
				pokemon.isNonstandard !== "CAP" &&
				!pokemon.name.toLowerCase().includes("-mega") &&
				!pokemon.name.toLowerCase().includes("-gmax")
		);

	// Fill alts
	for (const pokemon of sortedByPowerPokemons) {
		if (!pokemon.oob || !pokemon.oob.alts) continue;
		for (const alt of pokemon.oob.alts) {
			const formattedAltName = alt.toUpperCase().replace(/[^A-Z0-9]+/gi, " ");
			ALTS[formattedAltName] = pokemon;
		}
	}

	const allSpeciesNoLegendaries = sortedByPowerPokemons.filter(
		(pokemon) => !LEGENDARIES.has(pokemon.name.toUpperCase().replace(/[^A-Z0-9]+/gi, " "))
	);

	const tierThree = sortedByPowerPokemons.filter((pokemon) => sumPokemonBaseStats(pokemon) <= MAX_TIER_THREE_STATS);
	const tierTwo = sortedByPowerPokemons.filter(
		(pokemon) =>
			sumPokemonBaseStats(pokemon) > MAX_TIER_THREE_STATS && sumPokemonBaseStats(pokemon) <= MAX_TIER_TWO_STATS
	);
	const tierOne = sortedByPowerPokemons.filter((pokemon) => sumPokemonBaseStats(pokemon) > MAX_TIER_TWO_STATS);
	const tierOneNoLegendaries = allSpeciesNoLegendaries.filter(
		(pokemon) => sumPokemonBaseStats(pokemon) > MAX_TIER_TWO_STATS
	);
	let fileContent = "";
	// For each tier we will have a non type specific list and a type specific list
	const nameSuffix = "Species";
	const countNameSuffix = "SPECIES_COUNT";
	const lists = [
		{
			namePrefix: "tierThree",
			countNamePrefix: "TIER_THREE_",
			list: tierThree,
		},
		{
			namePrefix: "tierTwo",
			countNamePrefix: "TIER_TWO_",
			list: tierTwo,
		},
		{
			namePrefix: "tierOne",
			countNamePrefix: "TIER_ONE_",
			list: tierOne,
		},
		{
			namePrefix: "tierOneWithoutLegendaries",
			countNamePrefix: "TIER_ONE_WITHOUT_LEGENDARIES_",
			list: tierOneNoLegendaries,
		},
		{
			namePrefix: "all",
			countNamePrefix: "ALL_",
			list: sortedByPowerPokemons,
		},
		{
			namePrefix: "nonLegendary",
			countNamePrefix: "NON_LEGENDARY_",
			list: allSpeciesNoLegendaries,
		},
	];

	for (const option of lists) {
		fileContent += generateList(
			option.list,
			`${option.namePrefix}${nameSuffix}`,
			`${option.countNamePrefix}${countNameSuffix}`
		);
		for (const type of TYPES) {
			const pascalCaseType = type.charAt(0).toUpperCase() + type.slice(1).toLowerCase();
			const pokemonsOfType = option.list.filter((pokemon) => pokemon.types.includes(pascalCaseType));
			fileContent += generateList(
				pokemonsOfType,
				`${option.namePrefix}${pascalCaseType}${nameSuffix}`,
				`${option.countNamePrefix}${type}_${countNameSuffix}`
			);
			console.log(`${option.namePrefix} - ${type}: ${pokemonsOfType.length}`);
		}
	}
	console.log(tierThree.length, tierTwo.length, tierOne.length);

	fs.writeFileSync("./pokemon_tiers.h", fileContent);
	// fs.writeFileSync(`src/data/pokemon/species_info/gen_${genNumber}_families.h`, fileSplitedByLines.join("\n"));
}

